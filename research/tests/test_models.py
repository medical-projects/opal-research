"""
Unittests for the research.models module
"""
from opal.core.test import OpalTestCase
from opal.models import Team

from research import models

class StudySignalTestCase(OpalTestCase):
    def test_creates_study_team(self):
        models.ResearchStudy.objects.create(name='Test Study')
        team = Team.objects.get(name='test_study')
        self.assertEqual(True, team.restricted)
        self.assertEqual('Test Study', team.title)
        self.assertEqual(False, team.direct_add)



